    /// <summary>Marks an EF Core entity as tenant-scoped. Apply to a partial class.</summary>
    [AttributeUsage(AttributeTargets.Class, AllowMultiple = false, Inherited = false)]
    public sealed class TenantAttribute : Attribute { }

    /// <summary>Provides the current tenant identifier for the executing context (e.g. per HTTP request).</summary>
    public interface ITenantContext
    {
        string TenantId { get; }
    }

    /// <summary>Implemented by every class decorated with [Tenant].</summary>
    public interface ITenantEntity
    {
        string TenantId { get; set; }
    }

    /// <summary>
    /// EF Core SaveChangesInterceptor that stamps TenantId on every new ITenantEntity
    /// before the INSERT is issued. Register via AddTenantInterceptor().
    /// </summary>
    public sealed class TenantInterceptor : SaveChangesInterceptor
    {
        private readonly ITenantContext _tenantContext;

        public TenantInterceptor(ITenantContext tenantContext) => _tenantContext = tenantContext;

        public override InterceptionResult<int> SavingChanges(
            DbContextEventData eventData,
            InterceptionResult<int> result)
        {
            StampTenantIds(eventData.Context);
            return result;
        }

        public override ValueTask<InterceptionResult<int>> SavingChangesAsync(
            DbContextEventData eventData,
            InterceptionResult<int> result,
            CancellationToken cancellationToken = default)
        {
            StampTenantIds(eventData.Context);
            return base.SavingChangesAsync(eventData, result, cancellationToken);
        }

        private void StampTenantIds(DbContext? context)
        {
            if (context is null) return;
            foreach (var entry in context.ChangeTracker.Entries<ITenantEntity>())
                if (entry.State == EntityState.Added)
                    entry.Entity.TenantId = _tenantContext.TenantId;
        }
    }

    public static class TenantInterceptorExtensions
    {
        /// <summary>
        /// Registers a TenantInterceptor that stamps TenantId on new rows.
        /// Call this inside AddDbContext / OnConfiguring.
        /// </summary>
        public static DbContextOptionsBuilder AddTenantInterceptor(
            this DbContextOptionsBuilder optionsBuilder,
            ITenantContext tenantContext)
        {
            optionsBuilder.AddInterceptors(new TenantInterceptor(tenantContext));
            return optionsBuilder;
        }
    }

    public static class TenantModelBuilderExtensions
    {
        /// <summary>
        /// Adds a global EF Core query filter to every ITenantEntity so queries
        /// automatically return only rows belonging to the current tenant.
        /// Call this inside OnModelCreating.
        /// </summary>
        public static ModelBuilder AddTenantQueryFilters(
            this ModelBuilder modelBuilder,
            ITenantContext tenantContext)
        {
            foreach (var entityType in modelBuilder.Model.GetEntityTypes())
            {
                if (!typeof(ITenantEntity).IsAssignableFrom(entityType.ClrType))
                    continue;

                var param        = Expression.Parameter(entityType.ClrType, ""e"");
                var tenantIdProp = Expression.Property(param, nameof(ITenantEntity.TenantId));
                var tenantIdVal  = Expression.Property(
                    Expression.Constant(tenantContext),
                    nameof(ITenantContext.TenantId));
                var body   = Expression.Equal(tenantIdProp, tenantIdVal);
                var filter = Expression.Lambda(body, param);

                entityType.SetQueryFilter(filter);
            }

            return modelBuilder;
        }
    }
}
