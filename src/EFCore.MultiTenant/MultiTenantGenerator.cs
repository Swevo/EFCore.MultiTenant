    // ── Initialize ───────────────────────────────────────────────────────────
    public void Initialize(IncrementalGeneratorInitializationContext context)
    {
        context.RegisterPostInitializationOutput(ctx =>
            ctx.AddSource("EFCore.MultiTenant.Core.g.cs", CoreSource));

        var entities = context.SyntaxProvider
            .ForAttributeWithMetadataName(
                "EFCore.MultiTenant.TenantAttribute",
                predicate: static (n, _) => n is ClassDeclarationSyntax,
                transform: Transform)
            .Where(static x => x is not null)
            .Select(static (x, _) => x!);

        context.RegisterSourceOutput(entities, Generate);
    }

    // ── Transform ────────────────────────────────────────────────────────────
    private static TenantModel? Transform(GeneratorAttributeSyntaxContext ctx, CancellationToken ct)
    {
        if (ctx.TargetSymbol is not INamedTypeSymbol type) return null;

        var decl      = (ClassDeclarationSyntax)ctx.TargetNode;
        bool isPartial = decl.Modifiers.Any(SyntaxKind.PartialKeyword);

        var ns = type.ContainingNamespace is { IsGlobalNamespace: false } n
            ? n.ToDisplayString()
            : string.Empty;

        return new TenantModel
        {
            TypeName  = type.Name,
            Namespace = ns,
            IsPartial = isPartial
        };
    }

    // ── Code generation ──────────────────────────────────────────────────────
    private static void Generate(SourceProductionContext ctx, TenantModel model)
    {
        if (!model.IsPartial)
        {
            ctx.ReportDiagnostic(Diagnostic.Create(MTNT001, Location.None, model.TypeName));
            return;
        }

        var sb = new StringBuilder();
        sb.AppendLine("// <auto-generated by Swevo.EFCore.MultiTenant/>");
        sb.AppendLine("#nullable enable");
        sb.AppendLine();

        bool hasNs = !string.IsNullOrEmpty(model.Namespace);
        if (hasNs)
        {
            sb.AppendLine($"namespace {model.Namespace}");
            sb.AppendLine("{");
        }

        sb.AppendLine($"    public partial class {model.TypeName} : global::EFCore.MultiTenant.ITenantEntity");
        sb.AppendLine("    {");
        sb.AppendLine("        /// <summary>Identifies which tenant owns this row. Set automatically by TenantInterceptor on insert.</summary>");
        sb.AppendLine("        public string TenantId { get; set; } = string.Empty;");
        sb.AppendLine("    }");

        if (hasNs) sb.AppendLine("}");

        ctx.AddSource($"EFCore.MultiTenant.{model.TypeName}.g.cs", sb.ToString());
    }
